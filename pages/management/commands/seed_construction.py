"""
Команда наполнения БД демо-данными строительной компании «СтройМастер».

Использование:
    python manage.py seed_construction
"""
from django.core.management.base import BaseCommand
from django.utils import timezone


class Command(BaseCommand):
    help = 'Наполняет БД демо-данными строительной компании «СтройМастер»'

    def handle(self, *args, **options):
        from pages.management.commands.seed_base import clear_all, create_users, create_appearance

        self.stdout.write('Очистка БД...')
        clear_all()

        self.stdout.write('Создание пользователей...')
        create_users()

        self.stdout.write('Создание оформления...')
        theme, font = create_appearance(
            primary='#1e40af', background='#ffffff', surface='#f1f5f9',
            accent='#1d4ed8', text='#1e293b',
            font_name='Montserrat', font_google='Montserrat',
        )

        self.stdout.write('Создание настроек...')
        self._settings(theme, font)

        self.stdout.write('Создание страниц...')
        self._pages()

        self.stdout.write('Создание новостей...')
        self._news()

        self.stdout.write('Создание галереи...')
        self._gallery()

        self.stdout.write('Создание заявок...')
        self._leads()

        self.stdout.write(self.style.SUCCESS(
            '\nСтройМастер готов. http://127.0.0.1:8000/\n'
            'admin / admin1234\n'
            'director / director1234\n'
            'manager / manager1234\n'
            'editor / editor1234'
        ))

